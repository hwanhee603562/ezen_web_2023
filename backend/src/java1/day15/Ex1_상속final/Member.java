package java1.day15.Ex1_상속final;

public class Member {
	
	public final void stop() {
		// 오버라이딩 불가능한 메서드
	}
	
	
	
	// final 클래스명 : 상속x
	
	/*
	 	final 키워드	: final(최종적) 뜻으로 수정 불가능한 상태를 사용하는 키워드
	 		- 필드
	 			final int 나이 = 30;			: 수정 불가능한 필드	[ * 나이필드에 30값은 초기화 이후 변경불가능 ]
	 			
	  		- 클래스
	  			final class Member{}		: 해당 클래스는 상속불가능 [ * 상속불가 클래스 ]
	  			
	  		- 메서드
	  			final void stop(){}			: 해당 메서드는 자식클래스가 오버리이딩 불가능

		


	 */
	
	
	
	
	
	
	
}